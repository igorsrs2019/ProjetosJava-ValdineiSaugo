package exemplos;

public class ExmploTiposDeDados {
    public static void main(String[] args) {
        //inteiros - reais - logicos - textuais
        int valor01;
        float valor02;
        boolean valor03;
        char letra;
        valor01 = 12;
        valor02 = 12.78f;
        valor03 = true;
        letra = 'A';

        System.out.println(valor01);
        System.out.println("Conteúdo de valor02: " + valor02);
        System.out.println("Conteúdo de valor03: " + valor03);
        System.out.println("Conteúdo de letra: " + letra);
    }
}
